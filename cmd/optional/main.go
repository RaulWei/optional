// Optional is a tool that generates 'optional' type wrappers around a given type T.
//
// Typically this process would be run using go generate, like this:
//
//	//go:generate optional -type=Foo
//
// running this command
//
//	optional -type=Foo
//
// in the same directory will create the file optional_foo.go
// containing a definition of
//
//	type OptionalFoo struct {
//		...
//	}
//
// The default type is OptionalT or optionalT (depending on if the type is exported)
// and output file is optional_t.go. This can be overridden with the -output flag.
//
package main

import (
	"bytes"
	"flag"
	"fmt"
	"go/build"
	"go/format"
	"io/ioutil"
	"log"
	"os"
	"strings"
	"text/template"
	"time"
)

var (
	funcMap = template.FuncMap{
		"title": strings.Title,
		"first": func(s string) string {
			return strings.ToLower(string(s[0]))
		},
		"unexport": func(s string) string {
			return strings.ToLower(string(s[0])) + string(s[1:])
		},
	}
)

const tmpl = `// Code generated by go generate
// This file was generated by robots at {{ .Timestamp }}

package {{ .PackageName }}

// {{ .OutputName }} is an optional {{ .TypeName }}
type {{ .OutputName }} struct {
	{{ .TypeName | unexport }} {{ .TypeName }}
	present bool
}

// Empty{{ .OutputName | title }} returns an empty {{ .PackageName }}.{{ .OutputName }}
func Empty{{ .OutputName | title }}() {{ .OutputName }} {
	return {{ .OutputName }}{}
}

// Of{{ .TypeName | title }} creates a {{ .PackageName }}.{{ .OutputName }} from a {{ .TypeName }}
func Of{{ .TypeName | title }}({{ .TypeName | first }} {{ .TypeName }}) {{ .OutputName }} {
	return {{ .OutputName }}{ {{ .TypeName | unexport }}: {{ .TypeName | first }}, present: true}
}

// Set sets the {{ .TypeName }} value
func (o *{{ .OutputName }}) Set({{ .TypeName | first }} {{ .TypeName }}) {
	o.{{ .TypeName | unexport }} = {{ .TypeName | first }}
	o.present = true
}

// Get returns the {{ .TypeName }} value
func (o *{{ .OutputName }}) Get() {{ .TypeName }} {
	return o.{{ .TypeName | unexport }}
}

// Present returns whether or not the value is present
func (o *{{ .OutputName }}) Present() bool {
	return o.present
}

// OrElse returns the {{ .TypeName }} value or a default value if the value is not present
func (o *{{ .OutputName }}) OrElse({{ .TypeName | first }} {{ .TypeName }}) {{ .TypeName }} {
	if o.present {
		return o.{{ .TypeName | unexport}}
	}
	return {{ .TypeName | first }}
}`

type generator struct {
	packageName string
	outputName  string
	typeName    string
}

func (g *generator) generate() ([]byte, error) {
	t := template.Must(template.New("").Funcs(funcMap).Parse(tmpl))

	data := struct {
		Timestamp   time.Time
		PackageName string
		TypeName    string
		OutputName  string
	}{
		time.Now().UTC(),
		g.packageName,
		g.typeName,
		g.outputName,
	}

	var buf bytes.Buffer

	err := t.Execute(&buf, data)
	if err != nil {
		return nil, err
	}

	src, err := format.Source(buf.Bytes())
	if err != nil {
		return nil, err
	}
	return src, nil
}

func main() {
	log.SetFlags(0)
	log.SetPrefix("optional: ")

	typeName := flag.String("type", "", "type name; must be set")
	outputName := flag.String("output", "", "output type and file name; default [o|O]ptional<type> and srcdir/optional_<type>.go")

	flag.Parse()

	if len(*typeName) == 0 {
		flag.Usage()
		os.Exit(2)
	}

	pkg, err := build.Default.ImportDir(".", 0)
	if err != nil {
		log.Fatal(err)
	}

	var (
		filename string
		g        generator
	)

	g.typeName = *typeName
	g.packageName = pkg.Name

	if len(*outputName) == 0 {
		// no output specified, use default optional_<type>

		// TODO: may not be the most reliable method
		exported := strings.Title(g.typeName) == g.typeName

		if exported {
			g.outputName = "Optional" + strings.Title(g.typeName)
		} else {
			g.outputName = "optional" + strings.Title(g.typeName)
		}
		filename = fmt.Sprintf("optional_%s.go", strings.ToLower(g.typeName))
	} else {
		g.outputName = *outputName
		filename = strings.ToLower(g.outputName + ".go")
	}

	src, err := g.generate()
	if err != nil {
		log.Fatal(err)
	}

	err = ioutil.WriteFile(filename, src, 0644)
	if err != nil {
		log.Fatalf("writing output: %s", err)
	}
}
